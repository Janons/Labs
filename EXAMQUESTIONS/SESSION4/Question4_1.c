void string_search(char *str, int *start, int *lenght)
{
    int count;
    int i;

    while (start < lenght)
    {

        int count_max = -1;

        if (count > count_max)
        {
            count_max = count;
        }
        count = 0;

        while (str[i] = str[*start])
        {

            i++;
            count++;
        }
    }

    
}